package Model;

public class Ladder {
	
	private int base, top;
	
	public Ladder(int base, int top) {
		this.base = base;
		this.top = top;
	}

	public int getBase() {
		return base;
	}

	public void setBase(int base) {
		this.base = base;
	}

	public int getTop() {
		return top;
	}

	public void setTop(int top) {
		this.top = top;
	}
	
	
}
